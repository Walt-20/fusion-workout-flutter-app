class Entry {
  final String username;
  final String email;
  final String name;
  final String phoneNumber;
  final String age;
  final String sex;
  final String weight;
  final String height;
  final String availability;

  Entry({
    required this.username,
    required this.email,
    required this.name,
    required this.phoneNumber,
    required this.age,
    required this.sex,
    required this.weight,
    required this.height,
    required this.availability,
  });
}
